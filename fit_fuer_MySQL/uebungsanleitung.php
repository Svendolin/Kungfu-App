	<h2 class="braun">Fit für MySQL-Queries - Übungsanleitung</h2>
	<ul class="explanation">
		<li>Ziel dieser Übungssequenz ist es, dein Wissen über MySQL-Queries zu festigen.</li>
		<li>Erstelle eine neue Datenbank mit Hilfe von phpMyAdmin. Erzeuge darin eine Tabelle mit Hilfe der SQL-Datei in diesem Ordner: personen_utf8.sql. Du erhältst eine Tabelle namens &quot;Personen&quot;.</li>
		<li>Auf der nächsten Seite hat es kleine Kriminalfälle (Personenfahndung), die du lösen sollst. Formuliere in phpMyAdmin (im "SQL"-Assistent) zu jedem Kriminalfall eine Query.</li> 
		<li>Die Queries sollen möglichst genau formuliert werden. Das heisst also, es sollten <strong>möglichst wenig &quot;Täter&quot;</strong> in den Treffern zur Auswahl stehen.<br>
		Nicht alle Abfragen können jedoch so formuliert werden, dass nur eine Person gefunden wird. In diesen Fällen ist die Person mit ein wenig Logik zu bestimmen.</li>
		<li>Notiere deine Query-Strings bei den Lösungen (auf der übernächsten Seite).</li>
	</ul>
